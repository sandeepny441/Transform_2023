-- Length
SELECT LENGTH(column);

-- Lowercase
SELECT LOWER(column);

-- Uppercase   
SELECT UPPER(column);

-- Substring
SELECT SUBSTRING(column, start, length); 

-- Trim  
SELECT TRIM(column);

-- Left Trim
SELECT LTRIM(column);

-- Right Trim   
SELECT RTRIM(column);

-- Replace  
SELECT REPLACE(column, 'old', 'new');

-- Concatenate
SELECT CONCAT(column1, column2);

-- Reverse
SELECT REVERSE(column);  

-- Duplicate
SELECT REPEAT(column, n);

-- Split string
SELECT SUBSTRING_INDEX(column, delimiter, n);